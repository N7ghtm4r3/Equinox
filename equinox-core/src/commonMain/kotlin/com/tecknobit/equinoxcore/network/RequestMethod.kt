package com.tecknobit.equinoxcore.network

/**
 * `RequestMethod` list of available request methods
 */
enum class RequestMethod {

    /**
     * `"GET"` method for `"HTTP"` requests
     */
    GET,

    /**
     * `"POST"` method for `"HTTP"` requests
     */
    POST,

    /**
     * `"DELETE"` method for `"HTTP"` requests
     */
    DELETE,

    /**
     * `"PUT"` method for `"HTTP"` requests
     */
    PUT,

    /**
     * `"PATCH"` method for `"HTTP"` requests
     */
    PATCH

}